    private void showOldStudentData() {
        try {
            Student student = courseService.getStudentById(selectedId);
            nameTxt.setText(student.getName());
            surnameTxt.setText(student.getSurname());
            AddressTxt.setText(student.getAdress());
            String date1 = student.getDob();
            dobtxt.setToolTipText(date1);
            phoneTxt.setText(student.getPhone());
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

