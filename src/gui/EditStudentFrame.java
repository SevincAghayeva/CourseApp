    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        Student student = new Student();
        student.setName(nameTxt.getText());
        student.setSurname(surnameTxt.getText());
        student.setAdress(AddressTxt.getText());
        student.setDob(dobtxt.getDate() + "");
        student.setPhone(phoneTxt.getText());

        try {
            boolean isUpdated = courseService.updateStudent(student, selectedId);
            if (isUpdated) {
                JOptionPane.showMessageDialog(null, "Student has been updated successfully !");
//null default olaraq yazilir eslinde freymin adi yazilir orda
              

            } else {
                JOptionPane.showMessageDialog(null, "Problem!Student has not been updated successfully !", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_updateBtnActionPerformed

